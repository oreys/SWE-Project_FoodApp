namespace FoodApp
{
    /// <summary>
    /// This class represents the basic structure of a step in a recipe.
    /// </summary>
    public class Step
    {
        /// <summary>
        /// Represents the counter for all steps in one recipe.
        /// </summary>
        public int number;
        /// <summary>
        /// Represents what should be done in that step.
        /// </summary>
        public string description;
    }
}
